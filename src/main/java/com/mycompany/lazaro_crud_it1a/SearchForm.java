    private void btnUpdateActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnUpdateActionPerformed
        // TODO add your handling code here:
        
        id = txtuid.getText();
        fname= txtfname.getText();
        uname = txtuname.getText();
        pwd = txtpwd.getText();
        
        try{
            statement= con.createStatement();
            choice= JOptionPane.showConfirmDialog(null,"Update the record?");
        //if choice is yes
       
        if(choice==JOptionPane.YES_OPTION){
            statement.execute("UPDATE USERS SET " 
                    + " fullname = '" + fname + "',"
                    + "uname='" + uname + "',"
                    + "pwd = '" + pwd + "' where id ="
                    + id +";"); 
            
            JOptionPane.showMessageDialog(null, "Record Added!");
        }else{
            JOptionPane.showMessageDialog(null, "Update cancelled");
        }
        
        }catch(SQLException ex){
            JOptionPane.showMessageDialog(null,ex.getMessage());
        
        }finally{
            txtuid.setText("");
            txtfname.setText("");
            txtuname.setText("");
            txtpwd.setText("");
        }
    }//GEN-LAST:event_btnUpdateActionPerformed

    private void btndeleteActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btndeleteActionPerformed
        // TODO add your handling code here:
        id = txtuid.getText();
        try{
            choice = JOptionPane.showConfirmDialog(null, "Delete Record?");
            statement= con.createStatement();
            if(choice==JOptionPane.YES_OPTION){
                statement.execute("DELETE FROM users "
                        + "where id = " + id + ";");
                JOptionPane.showMessageDialog(null, "Record deletted");
            }else{
            JOptionPane.showMessageDialog(null, "Deletion cancelled!");
            }            
                
            }catch(SQLException ex){
                    JOptionPane.showMessageDialog(null,ex.getMessage());
                    
            }finally{
            txtuid.setText("");
            txtfname.setText("");
            txtuname.setText("");
            txtpwd.setText("");
        }  
    }//GEN-LAST:event_btndeleteActionPerformed

