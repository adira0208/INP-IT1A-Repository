    private void btnsearchActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnsearchActionPerformed
        // TODO add your handling code here:
        Suname = txtuname.getText();
        
        try{
             stmt = con.prepareStatement("SELECT id, fullname, uname, pwd from users where uname = ?" );
             stmt.setString(1, Suname);
             //execute the search query
             rs = stmt.executeQuery();
             if (rs.next()){
             
             id = rs.getString(1);
             fname = rs.getString(2);
             uname = rs.getString(3);
             pwd = rs.getString(4); 
             
             //set Textfield value from query result 
             txtuid.setText(id);
             txtfname.setText(fname);
             txtuname.setText(uname);
             txtpwd.setText(pwd);
             txtfname.setEditable(true);
             txtuname.setEditable(true);
             txtpwd.setEditable(true);
            
             }else{
                 JOptionPane.showMessageDialog(null,"Record not found!");
               //  Clear();
             }
             
             
        }catch(HeadlessException | SQLException e){
                    JOptionPane.showMessageDialog(null, e.getMessage());
                    
                    }    
      
    }//GEN-LAST:event_btnsearchActionPerformed

